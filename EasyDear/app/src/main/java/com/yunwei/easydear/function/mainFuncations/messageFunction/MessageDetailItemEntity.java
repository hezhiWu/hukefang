public class MessageDetailItemEntity {

    private String headUrl;
    private String messageContent;
    private String messageTime;
    private String messageConsumeInfo;

    public String getHeadUrl() {
        return headUrl;
    }

    public void setHeadUrl(String headUrl) {
        this.headUrl = headUrl;
    }

    public String getMessageContent() {
        return messageContent;
    }

    public void setMessageContent(String messageContent) {
        this.messageContent = messageContent;
    }

    public String getMessageTime() {
        return messageTime;
    }

    public void setMessageTime(String messageTime) {
        this.messageTime = messageTime;
    }

    public String getMessageConsumeInfo() {
        return messageConsumeInfo;
    }

    public void setMessageConsumeInfo(String messageConsumeInfo) {
        this.messageConsumeInfo = messageConsumeInfo;
    }
}
